package com.dh.veterinaria.model;

public class VeterinariaDTO {

    private int id;
    private String nombre;
    private int costo;

    //*************************************Contructores****************************************//

    //*************************************Getter y Setter*********************************************//

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getCosto() {
        return costo;
    }

    public void setCosto(int costo) {
        this.costo = costo;
    }


    //**************************************Metodos*********************************************//
    //************************************************************************************************//

}
